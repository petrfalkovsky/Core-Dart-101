/// Returns the result of concatenation of two strings.
///
/// @param {string} value1
/// @param {string} value2
/// @return {string}
///
/// @example
///   'aa', 'bb' => 'aabb'
///   'aa',''    => 'aa'
///   '',  'bb'  => 'bb'

concatenateStrings(srt1, srt2) {
  // throw Exception('Some arbitrary error');
  return srt1 + '' + srt2;
}


