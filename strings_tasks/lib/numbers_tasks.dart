/// Returns an area of a rectangle given by width and height.
///
/// @param {number} width
/// @param {number} height
/// @return {number}
///
/// @example:
///   5, 10 => 50
///   5, 5  => 25
int? getRectangleArea(width, height) {
  return width * height;
}
